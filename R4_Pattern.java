package Patterns;

public class R4_Pattern {

	public static void main(String[] args) {
		
		    

		        int n = 3;
		        int nst = 1;
		        int row = 1;

		        while (row <= n) {
		            for (int i = 0; i < nst; i++) {
		                System.out.print("*");
		            }

		            System.out.println();
		            row++;
		            nst++;
		        }
		    }
		


	

}
